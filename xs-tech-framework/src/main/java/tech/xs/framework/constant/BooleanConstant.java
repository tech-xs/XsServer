package tech.xs.framework.constant;

/**
 * 布尔值Constant
 * 数据库所有定义的布尔值均为数值类型
 * 为了扩展性考虑,所以定义布尔常量,此常量和字典表中boolean对应
 *
 * @author 沈家文
 * @date 2020/12/10 17:27
 */
public class BooleanConstant {

    /**
     * True
     * 启用
     * 存在
     */
    public static final int TRUE = 1;

    /**
     * False
     * 禁用
     * 不存在
     */
    public static final int FALSE = 2;

}
